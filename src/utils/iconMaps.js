// 레시피 아이콘 가져오기 함수
export const getRecipeIcon = (name) => {
  for (const [key, icon] of Object.entries(recipeIconMap)) {
    if (name.includes(key)) return icon;
  }
  return '🍳'; // 기본 아이콘
};

// 재료 아이콘 가져오기 함수
export const getIngredientIcon = (ingredient) => {
  return ingredientIconMap[ingredient] || ingredientIconMap.default;
};
